package Problems;

public class Problem4 {

    static byte byteValue;
    static short shortValue;
    static int intValue;
    static long longValue;
    static float floatValue;
    static double doubleValue;
    static char charValue;
    static boolean booleanValue;

    public static void main(String[] args) {
        /*
         * Problem 4:-
         * Declare one variable of each primitive data type:
         * byte, short, int, long, float, double, char, boolean.
         * Print their default values using a single System.out.println() per variable.
         */

        System.out.println("Default Byte: " + byteValue);
        System.out.println("Default Byte: " + shortValue);
        System.out.println("Default Byte: " + intValue);
        System.out.println("Default Byte: " + longValue);
        System.out.println("Default Byte: " + floatValue);
        System.out.println("Default Byte: " + doubleValue);
        System.out.println("Default Byte: " + charValue);
        System.out.println("Default Byte: " + booleanValue);
    }

}
